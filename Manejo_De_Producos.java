    private void TablaProductosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProductosMousePressed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TablaProductos.getModel();
        
        try{
        
        int filaSeleccionada = TablaProductos.getSelectedRow();
        
        /*
        Obtenemos los elementos de la dila seleccionada
        */
        Precios Precio = new Precios ((String)TablaProductos.getValueAt(filaSeleccionada, 0), 
                                      (String)TablaProductos.getValueAt(filaSeleccionada, 1),
                                      (double)TablaProductos.getValueAt(filaSeleccionada, 2),
                                      (double)TablaProductos.getValueAt(filaSeleccionada, 3));
                                      
        JOptionPane.showMessageDialog(null, Precio.getCodigo() + "" + Precio.getNombre() + "=" + 
                                            Precio.getPrecio() + "" + Precio.getCantidad());
        }catch(Exception e){
        System.out.println(e.getMessage());
        
        }
    }//GEN-LAST:event_TablaProductosMousePressed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        
    DefaultTableModel model = (DefaultTableModel) TablaProductos.getModel();
    
    int a =TablaProductos.getSelectedRow();
    
    
    if (a<0){
    
    JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
    
    }
    else {
    int confirmar = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar la fila selecionada de la tabla?"); 
    
    if(JOptionPane.OK_OPTION==confirmar){
        
        
        model.removeRow(a);
        JOptionPane.showMessageDialog(null, "Registro Eliminado");
      }
    }
    
        
    }//GEN-LAST:event_EliminarActionPerformed

