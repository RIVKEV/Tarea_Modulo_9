    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) TablaProductos.getModel();
        
        Precio.setCodigo(txtCodigo.getText());
        Precio.setNombre(txtNombre.getText());
        Precio.setPrecio(Double.parseDouble(txtPrecio.getText()));
        Precio.setCantidad(Double.parseDouble(txtCantidad.getText()));
        
        /*
        Crear un objeto para establecer la tabla
        */
    Object[] row = {Precio.getCodigo(), Precio.getNombre(),Precio.getPrecio(), Precio.getCantidad()}; 
    
    /*
    Establecer la fila al modelo de la tabla
    */
    model.addRow(row);
    }//GEN-LAST:event_AgregarActionPerformed

